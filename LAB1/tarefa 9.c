#include <stdio.h>
 
int main (){

    float a[3][3], a_1[3][3], det = 0;
    int i, j;

    printf("Insira a matriz: ");
    for(i = 0; i < 3; i++){
        for(j = 0; j < 3; j++){
            scanf("%f", &a[i][j]);
        }
    }
    printf("\n");

    det += a[0][0]*(a[1][1]*a[2][2] - a[1][2]*a[2][1]);
    det += a[0][1]*(a[1][2]*a[2][0] - a[1][0]*a[2][2]);
    det += a[0][2]*(a[1][0]*a[2][1] - a[1][1]*a[2][0]);

    if(det == 0){
        printf("A Matriz 'A' nao possui inversa.");
    }
    else{
        a_1[0][0] = +(a[1][1]*a[2][2] - a[1][2]*a[2][1]);
        a_1[0][1] = (-1)*(a[0][1]*a[2][2] - a[0][2]*a[2][1]);
        a_1[0][2] = +(a[0][1]*a[1][2] - a[0][2]*a[1][1]);

        a_1[1][0] = (-1)*(a[1][0]*a[2][2] - a[1][2]*a[2][0]);
        a_1[1][1] = +(a[0][0]*a[2][2] - a[0][2]*a[2][0]);
        a_1[1][2] = (-1)*(a[0][0]*a[1][2] - a[0][2]*a[1][0]);
        
        a_1[2][0] = +(a[1][0]*a[2][1] - a[1][1]*a[2][0]);
        a_1[2][1] = (-1)*(a[0][0]*a[2][1] - a[0][1]*a[2][0]);
        a_1[2][2] = +(a[0][0]*a[1][1] - a[0][1]*a[1][0]);
        
        printf("Inversa de A (A^-1): \n");
        for(i = 0; i < 3; i++){
            for(j = 0; j < 3; j++){
                printf("%.0f ", a_1[i][j]);
            }
            printf("\n");
        }
    }

    return 0;
}
